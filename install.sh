#!/bin/bash

# مسیرهای فایل‌ها
SCRIPT_PATH="/usr/local/bin/menu"

# تابع منوی اصلی
show_menu() {
    clear
    echo "========================================="
    echo "       Psiphon Manager Panel             "
    echo "========================================="
    echo "1) مشاهده لوکیشن‌ها و وضعیت سرویس"
    echo "2) راه‌اندازی مجدد سرویس‌ها"
    echo "3) حذف کامل اسکریپت و تمام فایل‌ها"
    echo "4) خروج"
    echo "========================================="
    read -p "لطفاً یک گزینه انتخاب کنید [1-4]: " choice

    case $choice in
        1)
            echo "در حال بررسی لوکیشن‌ها..."
            # کدهای مربوط به نمایش لوکیشن‌های شما اینجا قرار می‌گیرد
            sleep 3
            show_menu
            ;;
        2)
            echo "در حال ریستارت کردن سرویس‌ها..."
            # کدهای مربوط به ریستارت سرویس اینجا قرار می‌گیرد
            sleep 2
            show_menu
            ;;
        3)
            echo "شروع فرآیند حذف کامل اسکریپت..."
            uninstall_all
            ;;
        4)
            echo "خروج از پنل."
            exit 0
            ;;
        *)
            echo "گزینه نامعتبر است!"
            sleep 2
            show_menu
            ;;
    esac
}

# تابع حذف کامل (پاکسازی سرور)
uninstall_all() {
    echo "-----------------------------------------"
    echo "1. در حال متوقف کردن سرویس‌ها..."
    # اگر اسمی برای سرویس سیستم‌دی داری، جفت خط‌های زیر رو با اسم سرویس خودت ست کن:
    # systemctl stop psiphon.service 2>/dev/null
    # systemctl disable psiphon.service 2>/dev/null
    
    echo "2. در حال حذف فایل‌های ساختاری و سرویس‌ها..."
    # حذف فایل‌های سیستمی احتمالی (اسم سرویس خودت را جایگزین کن)
    # rm -f /etc/systemd/system/psiphon.service
    # systemctl daemon-reload
    
    echo "3. در حال حذف دسترسی میانبر منو..."
    rm -f "$SCRIPT_PATH"
    
    echo "4. در حال پاکسازی نهایی..."
    # اگر پوشه خاصی برای کدهات داری، مسیرش رو اینجا بذار تا پاک بشه:
    # rm -rf /etc/psiphon
    
    echo "========================================="
    echo "حذف با موفقیت انجام شد! تمام فایل‌ها پاک شدند."
    echo "========================================="
    exit 0
}

# تنظیم دستور میانبر برای دفعات بعدی
if [ ! -f "$SCRIPT_PATH" ]; then
    cp "$0" "$SCRIPT_PATH"
    chmod +x "$SCRIPT_PATH"
fi

# اجرای منو
show_menu
